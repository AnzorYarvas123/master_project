government control in the historically autonomous southeastern Levant. For the
 government, the Circassian settlers served the dual role as a periodic militia
 used against local rebellions and a key factor in the integration of the local 
economy through agricultural production, grain transportation, the construction
 and protection of the Hejaz Railway and service in local administrative bodies.
 The Circassian town of Amman grew rapidly after the construction of the Hejaz
 Railway, operational in central Transjordan since 1903, which also brought 
investment from Salti, Damascene, and Nabulsi merchants. As their numbers
 increased, the Circassians became a major local power and a number of pacts
 were formed with the Bedouin, including a mutual defense alliance with the
 Bani Sakhr in the late 1890s. The alliance proved instrumental in the Bani
 Sakhr's intervention in the 1906–1910 conflict between the Circassians and
 the Balqawiyya tribal confederation.The Circassian, Chechen and Turkmen 
settlements solidified the new sedentarized order taking place in the Balqa,
 which also included Salt and Karak townspeople and Bedouin tribesmen
 establishing their own agricultural and satellite villages. Two new roads linking
 Jerash and Amman were built via al-Ruman and Swaylih respectively to
 accommodate the settlers' ox-drawn carts, while secondary roads were built 
connecting Amman to its satellite Circassian and Chechen villages. Circa World
 War I there were 5,000–6,000 Circassians in Transjordan</p>
